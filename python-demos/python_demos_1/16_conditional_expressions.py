# This is Python's version of a ternary conditional statement

# if else code
i = 42
j = 3
if i > j:
    print("i is greater than j")
else:
    print("i is less than j")


# conditional expression code

print("i is greater than j") if i > j else print("i is less than j")

# inside the print parentheses
print("i is greater than j" if i > j else "i is less than j")
